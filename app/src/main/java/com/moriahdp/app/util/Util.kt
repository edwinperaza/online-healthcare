fun getFlagUrlByCountry(country: String): String {
    val base = "https://www.countryflags.io/"
    val end = "/flat/64.png"
    var flagUrl = base

    if (countryMap.containsKey(country)) {
        flagUrl += countryMap[country] + end
    }

    return flagUrl
}

